public class Coordinate {
    private double x;
    private double y;

    public Coordinate(double xCoor,double yCoor){
        this.x = xCoor;
        this.y = yCoor;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
